user = input("Zadej smajlíka: ")
if (user == ":-)"):
    print("Tvůj smajlík je veselý")
elif (user == ":-("):
    print("Tvůj smajlík je smutný")
elif (user == ":-*"):
    print("Tvůj smajlík posílá pusu")
elif (user == ":-P" or user == ":P"):
    print("Tvůj smajlík má vyplazený jazyk")
else:
    print("Tvůj smajlík je neznámý")